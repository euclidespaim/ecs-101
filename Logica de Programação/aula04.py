# Crie um programa que pede dois números para o usuário
# e imprimi as quatros operções na tela.


n1 = float(input("Digite um número: "))
n2 = float(input("Digite outro número: "))

# Esse bloco de código ira imprimir os resultados
print("A soma é: ", n1 + n2)
print("A subtração é: ", n1 - n2)
print("A multiplicação é: ", n1 * n2)
print("A divisão é: ", n1 / n2)
