#  Crie um programa que solicite dois números ao usuário: 
#  o número de produtos e 
#  o preço do produto.

#  O programa deve calcular o valor total da compra e exibir:
# "O total da sua compra é R$ [total]."


# Entrada de dados
n1 = float(input("Digite o número de produtos: "))
n2 = float(input("Digite o preço do produto: "))

# Processamento de dados
total = n1 * n2

# Saída de dados
print("O total da sua compra é R$ ", total)
